

import Foundation
import UIKit

extension UIColor {
    
    class func navigationGreyColor() -> UIColor {
        return UIColor(red: 232/255, green: 238/255, blue: 243/255, alpha: 1)
    }
    
    class func appBlueColor() -> UIColor {
        //46,150,239
        return UIColor(red: 46/255, green: 150/255, blue: 239/255, alpha: 1)
    }
}